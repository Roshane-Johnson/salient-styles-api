    /**
     * Display's the authenticated user.
     *
     * @return \Illuminate\Http\Response
     */
    public function authUser(Request $request)
    {
        $hasPerms = $request->user()->tokenCan("server:read");
        $request->user()->gradients = User::find($request->user()->id)->gradients;
        $request->user()->socials = User::find($request->user()->id)->socials;

        if ($hasPerms) {
            return success($request->user());
        }

        return error([], "Invalid permissions");
    }

